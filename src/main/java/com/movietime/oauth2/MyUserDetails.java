package com.movietime.oauth2;

import org.springframework.security.core.GrantedAuthority;
import org.springframework.security.core.userdetails.UserDetails;

import java.util.Collection;
import java.util.List;

/**
 * Created by Attila on 2015-05-07.
 * Provides core user information.
 * Implementations are not used directly by Spring Security for security purposes.
 * They simply store user information which is later encapsulated into Authentication objects.
 * This allows non-security related user information
 * (such as email addresses, telephone numbers etc) to be stored in a convenient location.
 */
public class MyUserDetails implements UserDetails {

    String username;
    String password;
    String email;
    List<? extends GrantedAuthority> authorities;

    public MyUserDetails() {}

    public MyUserDetails(String username, String password, String email, List<? extends GrantedAuthority> authorities) {
        this.username = username;
        this.password = password;
        this.email = email;
        this.authorities = authorities;
    }

    public void setUsername(String username) {
        this.username = username;
    }

    public void setPassword(String password) {
        this.password = password;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public void setAuthorities(List<? extends GrantedAuthority> authorities) {
        this.authorities = authorities;
    }

    @Override
    public Collection<? extends GrantedAuthority> getAuthorities() {
        return authorities;
    }

    @Override
    public String getPassword() {
        return password;
    }

    @Override
    public String getUsername() {
        return username;
    }

    @Override
    public boolean isAccountNonExpired() {
        return true;
    }

    @Override
    public boolean isAccountNonLocked() {
        return true;
    }

    @Override
    public boolean isCredentialsNonExpired() {
        return true;
    }

    @Override
    public boolean isEnabled() {
        return true;
    }
}
